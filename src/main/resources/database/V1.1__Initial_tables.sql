    C_CREATEDON TIMESTAMP, 
    C_DELETED BOOLEAN DEFAULT FALSE, 
    C_ID_NUMBER VARCHAR(64), 
    C_JOINT_STOCK DECIMAL(16, 2), 
    C_NAME VARCHAR(128), 
    C_REG_NUMBER VARCHAR(64), 
    C_FIRST_NAME VARCHAR(128), 
    C_ID_CARD_NUMBER VARCHAR(32), 
    C_ID_CARD_TYPE INTEGER, 
    C_LAST_NAME VARCHAR(128), 
    C_PERSONAL_NUMBER VARCHAR(32), 
    C_CHANGEDBY_ID INTEGER, 
    C_CREATEDBY_ID INTEGER, 
    C_LOCATION_ID INTEGER NOT NULL
);

