CREATE CACHED TABLE IF NOT EXISTS PUBLIC.T_EXPERTISE( 
    C_ID INTEGER AUTO_INCREMENT PRIMARY KEY, 
    C_CHANGEDON TIMESTAMP, 
    C_CREATEDON TIMESTAMP, 
    C_DELETED BOOLEAN NOT NULL, 
    C_LAST_CHECKED_ON TIMESTAMP, 
    C_NOTE VARCHAR(512), 
    C_NUMBER VARCHAR(32), 
    C_STATUS INTEGER, 
    C_TITLE VARCHAR(128), 
    C_YEAR INTEGER, 
    C_ADVANCE NUMERIC(16, 2), 
    C_PRICE NUMERIC(16, 2), 
    C_NEXT_HEARING DATE, 
    C_CHANGEDBY_ID INTEGER, 
    C_CREATEDBY_ID INTEGER, 
    C_COURT_ID INTEGER NOT NULL, 
    C_USER_ID INTEGER, 
    C_LOCATION_ID INTEGER 
);

